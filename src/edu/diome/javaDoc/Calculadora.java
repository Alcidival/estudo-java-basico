public class Calculadora {
    /**
     * Este métodos  é utilizado para somar dois números inteiros
     * @param numeroUm este é o primeiro parâmetro do métodos
     * @param numeroDois esé é o segundo parâmetro do métodos
     * @return 'int' o resultado deste métodos é a soma dos dois números
     */
    public int somar (int numeroUm, int numeroDois) {
        return numeroUm + numeroDois;
    }
}
