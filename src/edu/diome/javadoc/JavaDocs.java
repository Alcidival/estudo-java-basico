package edu.diome.javadoc;

public class JavaDocs {
    /**
     * TAGS
     * São informações que obtemos através de classes documentadas
     * na linguagem. Java Documentation é composto por TAGs que represetnam informações
     * relevantes para a documentação do projeto.
     */
    /**
     * TAG : DESCRIÇÃO
     * - @autor : Autor / Criador;
     * - @version: Versão do recurso disponibilizado;
     * - @since : Versão / Data de início da disponibilização do recurso;
     * - @param : Descrição dos parâmentros dos métodos criados;
     * - @return : Definição do tipo de retorno do métodos;
     * - @throws : Se o métodos lança alguma exceção;
     */
    /**
     * PARA GERAR DOCUMENTAÇÃO AUTOMÁTICA DA APLICAÇÃO
     * - executar no terminal
     * - javadoc -encoding UTF-8 -docencoding ISO-8859-1 -d ../docs src/*.java
     */
}
